namespace App\Form;

use App\Entity\Vertex;
use App\Service\Ollama\OutputConverter;
use App\Service\Ollama\RequestFactory;
use InvalidArgumentException;
use Symfony\Component\Form\AbstractType;
use Symfony\Component\Form\DataMapperInterface;
use Symfony\Component\Form\Extension\Core\Type\HiddenType;
use Symfony\Component\Form\Extension\Core\Type\SubmitType;
use Symfony\Component\Form\Extension\Core\Type\TextareaType;
use Symfony\Component\Form\FormBuilderInterface;
use Symfony\Component\Form\FormInterface;
use Symfony\Component\Form\FormView;
use Symfony\Component\OptionsResolver\OptionsResolver;
use Symfony\Component\Validator\Constraints\Callback;
use Symfony\Component\Validator\Constraints\NotBlank;
use Symfony\Component\Validator\Context\ExecutionContextInterface;
use Traversable;

/**
 * Append a output generated by a LLM to a content of a vertex
 */
class LlmOutputAppend extends AbstractType implements DataMapperInterface
{

    public function __construct(protected OutputConverter $converter, protected RequestFactory $payloadFactory)
    {
        
    }

    public function buildForm(FormBuilderInterface $builder, array $options): void
    {
        $builder
                ->add('prompt_query', HiddenType::class, ['constraints' => [new NotBlank()]])
                ->add('prompt_param', HiddenType::class, [
                    'constraints' => [
                        new NotBlank(),
                        new Callback(function ($value, ExecutionContextInterface $context) {
                                    $param = @json_decode($value, true);
                                    if (!key_exists('block_title', $param)) {
                                        $context->buildViolation('LLM generation parameters do not include an entry for block_title')
                                        ->addViolation();
                                    }
                                })
                    ]
                ])
                ->add('generation', TextareaType::class, ['attr' => ['x-model' => 'content', 'rows' => 30]])
                ->add('save', SubmitType::class)
                ->setMethod('PATCH')
                ->setDataMapper($this)
        ;
    }

    public function configureOptions(OptionsResolver $resolver): void
    {
        $resolver->setDefault('data_class', Vertex::class);
    }

    public function mapDataToForms(mixed $viewData, Traversable $forms): void
    {
        
    }

    public function mapFormsToData(Traversable $forms, &$viewData): void
    {
        if (!$viewData instanceof Vertex) {
            throw new InvalidArgumentException("Object viewdata is not a Vertex");
        }

        $field = iterator_to_array($forms);
        $promptParam = json_decode($field['prompt_param']->getData());
        $viewData->appendBlockWithTitle($promptParam->block_title, $this->converter->toWikitext($field['generation']->getData()));
    }

    public function finishView(FormView $view, FormInterface $form, array $options): void
    {
        // generating the API payload for Ollama with the prompt query
        $view->vars['payload'] = $this->payloadFactory->create($form['prompt_query']->getData());
    }

}
